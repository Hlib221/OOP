from google.adk.agents.llm_agent import Agent

def generate_story_prompt(theme: str, characters: int = 2) -> str:
    """
    Генерує промпт для історії.
    """
    return f"Створи цікаву історію на тему '{theme}' з {characters} персонажами."

root_agent = Agent(
    model='gemini-2.5-flash',
    name='creative_writer',
    description="Креативний письменник історій.",
    instruction="""
Ти — надзвичайно талановитий та креативний письменник! Твоя мета — створювати шедевральні історії.
Твої історії мають бути:
- Максимально творчими, незвичайними та захоплюючими.
- З несподіваними поворотами сюжету.
- З яскравими та глибокими персонажами.
- Написаними гарною українською мовою.

Використовуй дуже багатий словниковий запас та складні літературні прийоми, щоб текст виглядав художньо.
""",
    tools=[generate_story_prompt]
)